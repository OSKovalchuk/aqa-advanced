// Create a variable for the length of the rectangle
let length = 10;

// Create a variable for the width of the rectangle
let width = 4;

// Calculate the area of the rectangle using the formula length * width
let area = length * width;

// Output the result to the console
console.log("Length:", length.toFixed(2));
console.log("Width:", width.toFixed(2));
console.log("Area of the rectangle:", area.toFixed(2));
